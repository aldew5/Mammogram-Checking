// modified Fisher-Yates Shuffle
function shuffle(array: any) {
    let currentIndex: number = array.length, randomIndex;

    // While there remain elements to shuffle...
    while (0 !== currentIndex) {
        // Pick a remaining element
        randomIndex = Math.floor(Math.random() * currentIndex);
        currentIndex -= 2;

        if (randomIndex % 2 !== 0) {
            randomIndex += 1;
        }

        // And swap it with the current element.
        [array[currentIndex], array[randomIndex]] = [
            array[randomIndex], array[currentIndex]];

        [array[currentIndex + 1], array[randomIndex + 1]] =
            [array[randomIndex + 1], array[currentIndex + 1]];
    }

    // facing outward
    for (let i = 0; i + 1 < images.length; i += 2) {
        let first: any[] = images[i];
        let second: any[] = images[i + 1];

        images[i] = second;
        images[i + 1] = first;
    }

    return array;
}

